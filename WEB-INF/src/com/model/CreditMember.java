package com.model;

public class CreditMember 
{
	private String loginid;
	private String cardnum;
	private String name;
	private String bankname;
	private String cvvnum;
	private String exmonth;
	private String exyear;
	private String amount;
	public String getLoginid() {
		return loginid;
	}
	public void setLoginid(String loginid) {
		this.loginid = loginid;
	}
	public String getCardnum() {
		return cardnum;
	}
	public void setCardnum(String cardnum) {
		this.cardnum = cardnum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBankname() {
		return bankname;
	}
	public void setBankname(String bankname) {
		this.bankname = bankname;
	}
	public String getCvvnum() {
		return cvvnum;
	}
	public void setCvvnum(String cvvnum) {
		this.cvvnum = cvvnum;
	}
	public String getExmonth() {
		return exmonth;
	}
	public void setExmonth(String exmonth) {
		this.exmonth = exmonth;
	}
	public String getExyear() {
		return exyear;
	}
	public void setExyear(String exyear) {
		this.exyear = exyear;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	
	
	
}

